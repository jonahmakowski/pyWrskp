import turtle

def display_example():
    turtle.color('blue')
    turtle.penup()
    turtle.setpos(0, 100)
    turtle.pendown()
    turtle.left(90)
    turtle.forward(100)
    
    turtle.penup()
    turtle.setpos(50, 150)
    turtle.pendown()
    turtle.pensize(10)
    turtle.setheading(0)
    turtle.backward(100)

